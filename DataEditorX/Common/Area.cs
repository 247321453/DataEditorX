
namespace DataEditorX.Common
{
    /// <summary>
    /// 区域
    /// </summary>
    public class Area
    {
        public Area()
        {
            left = 0;
            top = 0;
            width = 0;
            height = 0;
        }
        /// <summary>
        /// 左
        /// </summary>
        public int left;
        /// <summary>
        /// 顶
        /// </summary>
        public int top;
        /// <summary>
        /// 宽
        /// </summary>
        public int width;
        /// <summary>
        /// 高
        /// </summary>
        public int height;
    }
}
